#pragma once
#include "Matrix_CPU.hpp"

namespace julie
{
namespace la
{
namespace cpu
{

/*********************************************************************************
 * This is the base class of all CPU mode activation functions that hold one
 * trainable scalar parameter. For example: PReLU, Swish, etc. Activation functions
 * like ReLU or Sigmoid that do not hold trainable parameters will not derive from
 * this base class.
 *********************************************************************************/
template <typename DT>
class Activation_CPU_adv
{
public:

    // Default constructor
    Activation_CPU_adv() {}

    // Forward operation of the activation giving the activation's derivatives
    // Arguments:
    //     output:     Output of this activation function
    //     diff:       The derivative d(output) / d(input) generated by this activation function
    //     alpha_diff: The derivative d(output) / d(alpha) generated by this activation function
    //     in:         Input of this activation function
    //     alpha:      The trainable scalar input of this activation function
    // Returns: void
    virtual void operator () (Matrix_CPU<DT> & output, Matrix_CPU<DT> & diff, Matrix_CPU<DT> & alpha_diff, const Matrix_CPU<DT> & in, const DT & alpha) = 0;

    // Forward operation of the activation without providing the activation's derivatives
    // Arguments:
    //     output: Output of this activation function
    //     in:     Input of this activation function
    //     alpha:  The trainable scalar input of this activation function
    // Returns:    void
    virtual void operator () (Matrix_CPU<DT> & output, const Matrix_CPU<DT> & in, const DT & alpha) = 0;
};

template <typename DT>
class PReLU : public Activation_CPU_adv<DT>
{
public:

    // Forward operation of the activation giving the activation's derivatives
    // Arguments:
    //     output:     Output of this activation function
    //     diff:       The derivative d(output) / d(input) generated by this activation function
    //     alpha_diff: The derivative d(output) / d(alpha) generated by this activation function
    //     in:         Input of this activation function
    //     alpha:      The trainable scalar input of this activation function
    // Returns: void
    void operator () (Matrix_CPU<DT> & output, Matrix_CPU<DT> & diff, Matrix_CPU<DT> & alpha_diff, const Matrix_CPU<DT> & in, const DT & alpha);

    // Forward operation of the activation without providing the activation's derivatives
    // Arguments:
    //     output: Output of this activation function
    //     in:     Input of this activation function
    //     alpha:  The trainable scalar input of this activation function
    // Returns:    void
    void operator () (Matrix_CPU<DT> & output, const Matrix_CPU<DT> & in, const DT & alpha);
};


} // namespace cpu
} // namespace la
} // namespace julie